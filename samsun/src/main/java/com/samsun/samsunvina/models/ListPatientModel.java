package com.samsun.samsunvina.models;

public class ListPatientModel {

    private String employeeName;

    private String patientName;

    private String patientChart;

    public ListPatientModel() {
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getPatientName() {
        return patientName;
    }

    public void setPatientName(String patientName) {
        this.patientName = patientName;
    }

    public String getPatientChart() {
        return patientChart;
    }

    public void setPatientChart(String patientChart) {
        this.patientChart = patientChart;
    }
}
